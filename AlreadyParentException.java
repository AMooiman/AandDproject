
public class AlreadyParentException extends Exception {
	/**
	 * 
	 */
	private static final long serialVersionUID = 4030309071834797724L;
	public AlreadyParentException() { super(); }
	public AlreadyParentException(String message) { super(message); }
	public AlreadyParentException(String message, Throwable cause) { super(message, cause); }
	public AlreadyParentException(Throwable cause) { super(cause); }
}
